#!/usr/bin/env python3
"""
Stratified sampler for building an INDEPENDENT gold-label set.

The problem this solves
-----------------------
evaluation/manual_labels.json is generated by build_manual_labels.py using the
SAME heuristics the ranker scores with -> evaluating against it measures
self-consistency, not quality. To get a real signal we need labels produced
INDEPENDENTLY of the scoring code.

This script does NOT score or label. It only pulls a stratified, reproducible
sample of real candidates across the archetypes the JD cares about, and dumps
their full profiles to a human-readable file. A human (or a careful reading
pass) then assigns a 0-4 relevance tier per candidate in gold_labels.json.

Archetype strata (so the gold set exercises the traps, not just easy fits):
  - strong_title    : AI/ML engineer titles, 5-9 yoe
  - plain_language  : non-AI title but career desc shows retrieval/ranking/recsys
  - keyword_stuffer : many AI skills but non-tech current title
  - consulting_only : entire career at services firms
  - inactive        : strong-ish profile but stale / low response rate
  - honeypot_like   : timeline/skill impossibilities
  - random          : uniform sample for calibration

Usage:
  python evaluation/sample_for_gold.py \
      --candidates "../[PUB] .../candidates.jsonl" \
      --per-stratum 12 --out evaluation/gold_sample.txt
"""

import argparse
import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent.parent))

import config
from pipeline.loader import load_candidates
from pipeline.honeypot_detector import detect_honeypot

AI_TITLE_KW = ("ai engineer", "ml engineer", "machine learning", "nlp engineer",
               "data scientist", "applied scientist", "research engineer")
NON_TECH_KW = ("marketing", "sales", "hr", "human resources", "accountant",
               "operations manager", "civil engineer", "mechanical engineer",
               "content writer", "graphic designer", "recruiter", "project manager")
AI_SKILL_KW = ("machine learning", "deep learning", "nlp", "pytorch", "tensorflow",
               "embeddings", "transformers", "llm", "bert", "rag", "vector",
               "pinecone", "faiss", "elasticsearch")
RETRIEVAL_DESC_KW = ("ranking", "retrieval", "recommendation", "recsys", "search",
                     "embedding", "vector", "information retrieval", "relevance")


def _skills_lower(c):
    return [s.get("name", "").lower() for s in c.get("skills", [])]


def _career_text(c):
    return " ".join(r.get("description", "").lower() for r in c.get("career_history", []))


def classify(c):
    """Return the stratum label for a candidate (first match wins)."""
    prof = c.get("profile", {})
    title = prof.get("current_title", "").lower()
    yoe = prof.get("years_of_experience", 0)
    skills = _skills_lower(c)
    career = c.get("career_history", [])
    ctext = _career_text(c)

    is_hp, _ = detect_honeypot(c)
    if is_hp:
        return "honeypot_like"

    ai_skill_count = sum(1 for s in skills if any(k in s for k in AI_SKILL_KW))
    is_non_tech = any(k in title for k in NON_TECH_KW)
    if is_non_tech and ai_skill_count >= 4:
        return "keyword_stuffer"

    companies = [r.get("company", "").lower().strip() for r in career]
    if companies and all(co in config.CONSULTING_FIRMS for co in companies):
        return "consulting_only"

    has_ai_title = any(k in title for k in AI_TITLE_KW)
    if has_ai_title and 5 <= yoe <= 9:
        return "strong_title"

    if not has_ai_title and any(k in ctext for k in RETRIEVAL_DESC_KW):
        return "plain_language"

    sig = c.get("redrob_signals", {})
    if sig.get("recruiter_response_rate", 1.0) < 0.15 or not sig.get("open_to_work_flag", True):
        return "inactive"

    return "random"


def render(c):
    """Human-readable dump of one candidate for manual tier assignment."""
    prof = c.get("profile", {})
    sig = c.get("redrob_signals", {})
    lines = [
        f"  Title       : {prof.get('current_title')} @ {prof.get('current_company')} ({prof.get('current_company_size')})",
        f"  Experience  : {prof.get('years_of_experience')} yrs | {prof.get('location')}, {prof.get('country')} | industry: {prof.get('current_industry')}",
        f"  Headline    : {prof.get('headline')}",
        f"  Summary     : {prof.get('summary','')[:400]}",
        f"  Skills      : {', '.join(s.get('name','')+'('+s.get('proficiency','?')[:3]+')' for s in c.get('skills', [])[:18])}",
    ]
    for r in c.get("career_history", [])[:3]:
        lines.append(f"  Role        : {r.get('title')} @ {r.get('company')} ({r.get('duration_months')}mo) — {r.get('description','')[:220]}")
    lines.append(f"  Signals     : open_to_work={sig.get('open_to_work_flag')} resp_rate={sig.get('recruiter_response_rate')} "
                 f"last_active={sig.get('last_active_date')} notice={sig.get('notice_period_days')}d "
                 f"github={sig.get('github_activity_score')} saved={sig.get('saved_by_recruiters_30d')}")
    return "\n".join(lines)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--candidates", required=True)
    ap.add_argument("--per-stratum", type=int, default=12)
    ap.add_argument("--out", default="evaluation/gold_sample.txt")
    ap.add_argument("--ids-out", default="evaluation/gold_sample_ids.json")
    args = ap.parse_args()

    strata = {k: [] for k in ["strong_title", "plain_language", "keyword_stuffer",
                              "consulting_only", "inactive", "honeypot_like", "random"]}

    # Deterministic stratified reservoir: walk in id order, fill each stratum.
    for c in load_candidates(args.candidates):
        s = classify(c)
        if len(strata[s]) < args.per_stratum:
            strata[s].append(c)
        if all(len(v) >= args.per_stratum for v in strata.values()):
            break

    out_lines = []
    id_map = {}
    for s, cands in strata.items():
        out_lines.append("\n" + "=" * 78)
        out_lines.append(f"STRATUM: {s}  ({len(cands)} candidates)")
        out_lines.append("=" * 78)
        for c in cands:
            cid = c["candidate_id"]
            id_map[cid] = s
            out_lines.append(f"\n[{cid}]  (stratum={s})")
            out_lines.append(render(c))

    Path(args.out).write_text("\n".join(out_lines), encoding="utf-8")
    Path(args.ids_out).write_text(json.dumps(id_map, indent=2), encoding="utf-8")
    print(f"Wrote {len(id_map)} candidates across {len(strata)} strata to {args.out}")
    for s, cands in strata.items():
        print(f"  {s:16s}: {len(cands)}")


if __name__ == "__main__":
    main()
